                                <p> When Chitral was a state Nagar Fort had a significant importance being on the route of invading armies from Afghanistan. It was built on the orders of the then Mehtar of Chitral, HH Sir Shuja ul Mulk, originally constructed as a winter resort for the Mehtar, since it is in an area that is relatively warmer than other parts of Chitral being situated at an altitude of 1209meters above sea level. As you come down from Lowari pass and reach the Chitral river at Mirkhani post its just two km ahead on the river bank. Perched on a ridge, surrounded by water from three sides you cant miss the picturesque view. </p><p>The Fort is accessible through a suspension bridge, it has an Outer gate, houses an Outer Court, a Inner Gate and an Inner Courtyard. The late Prince Sirajuddin of Nagar was a keen horticulturist and had surrounded his fort with one of the most picturesque gardens, with a variety of fruits. His hospitality made all travelers welcome to stop by and helped bring Chitral a good name. His son Prince Salahuddin has continued with the tradition , infact gone a step ahead to introduce many more varieties to the Fort. A profusion of roses, daffodils and herbaceous plants scent the air. March, April and May are months when the gardens are full of flowering trees and shrubs, while the other months have many interesting delights. Majestic views of the surrounding mountains can be fully appreciated from the Nagar Fort.</p>
