One of the major attractions of Pakistan Cultural Festivals are Kalash Valleyâ€™s Festivals. The Kalash Valleys of Rumbur, Bumboret and Birir are within Chitral in the North-West Frontier Province, near Nuristan in Afghanistan. The people here are some of the only non-Muslims for hundreds of miles. The home of kafir Kalash or wearers of the black rose, a primitive pagan tribe.The Kalash are an ancient tribe and have a religion and culture of their own, their culture is unique as well as an amazing one. A legend says that one General Salik Shah, who was called by the Greek as General Sulfurous with five soldiers of the legions of Alexander of MACEDON, settled in Chitral and that they are the progenitors of the Kafir Kalash. The Kalash are infamous for their festivals; these folks know how to let their hair down in style. There is much dancing where the elders chant legends with drums accompaniment and the women dance around outside. Locally brewed Mulberry and Grape wine is drunk in profuse quantities. The Kalash People celebrate three main festivals in a year. </p><p>


