                                <p> The word Galiyat is the plural of the Urdu word gali, which means an alley between two mountains on both sides of which there are valleys and it is not the highest point in the range. Many of the towns in the area have the word gali as part of their names. These are popular summer-holiday destinations in Pakistan. Murree, one of the most popular hill stations of Pakistan, is the largest town of the Galiyat area.</p><p>

It is strategically located between the Northern Areas, Azad Kashmir, Punjab and the rest of the Khyber Pukhtunkhwa. Hill stations collectively known as the Gallis and the Kaghan valley are the best known parts of Hazara. Domestic and foreign tourists throng its cool and serene valleys during sizzling summer.</p>
<P>
The region of Hazara is unique on our planet, for it opens up to visitors, a world replete with natural beauty. It was a favorite gateway from the plains into Kashmir during Mughal, Durrani and Sikh rules. It is a large area some 10,000 sq. km. The world famous Kara koram Highway starts from Havelian, a town in this region at a distance of 101 km from Islamabad. The mighty “Lion River” Indus or Abasin flows through the entire length of Hazara.</P>
<p>
Charra Pani is a tiny hill station in the Galiyat region of northern Pakistan. Dunga Gali or Doonga Gali is one of the tourist mountain resort towns of the Galiyat area of Pakistan, it has an altitude of 2500m.Dunga Gali is located next to Nathia Gali which is a Union Council (i.e. subdivision) of Abbottabad District in the Khyber Pukhtunkhwa of Pakistan. During British rule it served as a sanatorium and contained a hotel, a post office and a small church. The area was visited by Europeans during summer, which also had houses on the southern slopes of the nearby Mukeshpuri hill.
</p><p>
Bara Gali is one of the tourist mountain resort towns of the Galiyat area is situated at, an altitude of 2350m. Bara Gali lies on the road between Abbottabad and Murree; it is 15 miles from Abbottabad and 25 miles from Murree. Bara Gali is also the summer campus of the University of Peshawar.</p>
<p>
Changla Gali is one of the tourist mountain resort towns of the Galiyat area of Pakistan, it has an altitude of 2559m.During British rule it was the headquarters of the Northern Command School of Musketry.</p>
